    /**
     * Extract the General Rules section from SKILL.md.
     * This section applies to ALL agents and is always included.
     */
    public function extractGeneralRules(): string
    {
        $sections = $this->parseSections();
        return $sections['general_rules'] ?? '';
    }

    /**
     * Extract the section for a specific agent by number.
     * Returns the agent's heading, table, and behavior rules.
     */
    public function extractAgentSection(int $agentNumber): string
    {
        $sections = $this->parseSections();
        return $sections['agents'][$agentNumber] ?? '';
    }

    /**
     * Extract the Anti-Hallucination Rules (Detailed) section.
     */
    public function extractAntiHallucinationRules(): string
    {
        $sections = $this->parseSections();
        return $sections['anti_hallucination'] ?? '';
    }

    /**
     * Parse SKILL.md into named sections (cached per request).
     */
    private function parseSections(): array
    {
        if ($this->parsedSections !== null) {
            return $this->parsedSections;
        }

        $content = $this->getSkillContent();
        $lines = explode("\n", $content);
        $this->parsedSections = [
            'general_rules' => '',
            'agents' => [],
            'anti_hallucination' => '',
            'self_correction' => '',
            'error_memory' => '',
        ];

        $currentSection = null;
        $currentAgentNumber = null;
        $buffer = [];

        foreach ($lines as $line) {
            // Detect ## القواعد العامة (General Rules — Arabic SKILL.md)
            if (preg_match('/^## القواعد العامة/u', $line)) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = 'general_rules';
                $currentAgentNumber = null;
                $buffer = [$line];
                continue;
            }

            // Detect ### الوكيل N: Name (Agent N — Arabic SKILL.md)
            if (preg_match('/^### الوكيل (\d+):/u', $line, $m)) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = 'agent';
                $currentAgentNumber = (int) $m[1];
                $buffer = [$line];
                continue;
            }

            // Detect ## قواعد مكافحة الهلوسة (Anti-Hallucination Rules — Arabic SKILL.md)
            if (preg_match('/^## قواعد مكافحة الهلوسة/u', $line)) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = 'anti_hallucination';
                $currentAgentNumber = null;
                $buffer = [$line];
                continue;
            }

            // Detect ## قواعد التصحيح الذاتي (Self-Correction Rules — Arabic SKILL.md)
            if (preg_match('/^## قواعد التصحيح الذاتي/u', $line)) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = 'self_correction';
                $currentAgentNumber = null;
                $buffer = [$line];
                continue;
            }

            // Detect ## صيغة ذاكرة الأخطاء (Error Memory Format — Arabic SKILL.md)
            if (preg_match('/^## صيغة ذاكرة الأخطاء/u', $line)) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = 'error_memory';
                $currentAgentNumber = null;
                $buffer = [$line];
                continue;
            }

            // Detect other ## headings — flush current section
            if (preg_match('/^## /', $line) && $currentSection !== null) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = null;
                $currentAgentNumber = null;
                $buffer = [];
                continue;
            }

            // For agent sections, stop at --- separator (between agents)
            if ($currentSection === 'agent' && preg_match('/^---\s*$/', $line)) {
                $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);
                $currentSection = null;
                $currentAgentNumber = null;
                $buffer = [];
                continue;
            }

            if ($currentSection !== null) {
                $buffer[] = $line;
            }
        }

        // Flush any remaining buffer
        $this->flushBuffer($currentSection, $currentAgentNumber, $buffer);

        return $this->parsedSections;
    }

    private function flushBuffer(?string $section, ?int $agentNumber, array $buffer): void
    {
        if ($section === null || empty($buffer)) {
            return;
        }

        $text = trim(implode("\n", $buffer));
        if ($text === '') {
            return;
        }

        if ($section === 'agent' && $agentNumber !== null) {
            $this->parsedSections['agents'][$agentNumber] = $text;
        } elseif ($section === 'general_rules') {
            $this->parsedSections['general_rules'] = $text;
        } elseif ($section === 'anti_hallucination') {
            $this->parsedSections['anti_hallucination'] = $text;
        } elseif ($section === 'self_correction') {
            $this->parsedSections['self_correction'] = $text;
        } elseif ($section === 'error_memory') {
            $this->parsedSections['error_memory'] = $text;
        }
    }

