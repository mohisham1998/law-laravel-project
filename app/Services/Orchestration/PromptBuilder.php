    /**
     * Build a focused prompt for a specific agent.
     *
     * Composition:
     *  1. General Rules (from SKILL.md)
     *  2. Agent-specific section (from SKILL.md)
     *  3. Anti-Hallucination Rules (for agents that cite statutes)
     *  4. Output template with few-shot example
     *  5. Context boundary instruction
     *  6. Case context
     *
     * @deprecated Use buildSystemPrompt() + buildUserPrompt() separately for new callers.
     *             This method remains for backward compatibility.
     */
    public function buildPromptForAgent(int $agentNumber, string $context): string
    {
        $parts = [];

        // 1. General Rules (always included)
        $generalRules = $this->extractGeneralRules();
        if ($generalRules) {
            $parts[] = $generalRules;
        }

        // 2. Agent-specific section
        $agentSection = $this->extractAgentSection($agentNumber);
        if ($agentSection) {
            $parts[] = $agentSection;
        } else {
            // Fallback for agents not found in SKILL.md (shouldn't happen)
            $parts[] = "## Agent {$agentNumber}\n\nExecute the task for Agent {$agentNumber}.";
        }

        // 3. Anti-Hallucination Rules (for agents that produce/consume statute citations: 3,5,6,7,8,9)
        if (in_array($agentNumber, [3, 5, 6, 7, 8, 9])) {
            $antiHallucination = $this->extractAntiHallucinationRules();
            if ($antiHallucination) {
                $parts[] = $antiHallucination;
            }
        }

        // 4. Output template
        $template = $this->buildOutputTemplate($agentNumber);
        if ($template) {
            $parts[] = $template;
        }

        // 5. Context boundary instruction (for agents that cite statutes)
        if (in_array($agentNumber, [5, 6, 7, 8, 9])) {
            $parts[] = "---\n## قيد إلزامي\n\nلا يجوز الاستشهاد بأي نظام أو مادة غير مذكورة في السياق أدناه. أي استشهاد بمادة غير موجودة في فهرس المواد النظامية المقدم يُعتبر خطأً جسيمًا وسيتم رفضه تلقائيًا.";
        }

        // 6. Case context
        $parts[] = "---\n## سياق القضية\n\n{$context}";

        return implode("\n\n---\n\n", $parts);
    }

    /**
     * Build output template with expected format for each agent.
     * Includes few-shot examples for critical agents (5, 6, 8).
     */
    public function buildOutputTemplate(int $agentNumber): string
    {
        return match ($agentNumber) {
            0 => $this->templateAgent0(),
            1 => $this->templateAgent1(),
